/* -------------------------------------------------------------------------- */
/*                            User Model Class                                  */
/* -------------------------------------------------------------------------- */
class UserModel {
  /* -------------------------------------------------------------------------- */
  /*                            Properties                                        */
  /* -------------------------------------------------------------------------- */
  final String id;
  final String name;
  final String email;

  /* -------------------------------------------------------------------------- */
  /*                            Constructor                                       */
  /* -------------------------------------------------------------------------- */
  UserModel({
    required this.id,
    required this.name,
    required this.email,
  });

  /* -------------------------------------------------------------------------- */
  /*                            JSON Conversion Methods                           */
  /* -------------------------------------------------------------------------- */
  UserModel.fromJson(Map<String, dynamic> json)
      : this(
          id: json['id'],
          name: json['name'],
          email: json['email'],
        );

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'name': name,
      'email': email,
    };
  }
}
